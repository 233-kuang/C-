#include<iostream>
using namespace std;

int main()
{
	//sizeof统计数据类型所占空间的大小
	cout << "short的大小" << sizeof(short) << endl;
	cout << "int的大小" << sizeof(int) << endl;
	cout << "float的大小" << sizeof(float) << endl;
	cout << "long long的大小" << sizeof(long long) << endl;
	cout << "double的大小" << sizeof(double) << endl;
	return 0;
}